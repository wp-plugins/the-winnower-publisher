<p>
  You'll need an account with The Winnower. Yourself and your authors will require a full name in their wordpress profile. Once you've activated the plugin by setting your API key below, authors will be able to enable cross posting on their posts.

  <h3>How to Cross Post</h3>
  <ol>
    <li>On new or existing posts find "The Winnower Post Settings"</li>
    <li>Find "Cross-Post to The Winnower" and choose "Yes"</li>
    <li>Chose at least one Topic for the post</li>
    <li>Click "Publish"</li>
    <li>Your post will now be posted to the winnower!</li>
  </ol>
  Your post will now be posted to the winnower!
</p>
<p>
  <h3>Getting your DOI</h3>
  Currently only administrators may request a DOI.
  <ol>
    <li>Once a post has been published, click "You can request a DOI here" in "The Winnower Post Settings". You will be redirected to the post's page at thewinnower.com. (eg. https://thewinnower.com/posts/what-are-winnower-authors-doing)</li>
    <li>On your post's page, click "Revise this Paper"</li>
    <li>Click "Archive" - Note: Once you archive your post it can no longer be updated.</li>
    <li>You now have a DOI!</li>
    <li>Go back to your post's edit page in your blog and click "Retrieve DOI".</li>
    <li>Now update your paper to save the DOI.</li>
  </ol>
  The DOI will now display for citation at the bottom of you post!
</p>
